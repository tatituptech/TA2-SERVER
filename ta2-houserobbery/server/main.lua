local TA2Core = exports['ta2-core']:GetCoreObject()

-- Functions

local function ResetHouseStateTimer(house)
    CreateThread(function()
        Wait(Config.TimeToCloseDoors * 60000)
        Config.Houses[house]['opened'] = false
        for _, v in pairs(Config.Houses[house]['furniture']) do
            v['searched'] = false
        end
        TriggerClientEvent('ta2-houserobbery:client:ResetHouseState', -1, house)
    end)
end

-- Callbacks

TA2Core.Functions.CreateCallback('ta2-houserobbery:server:GetHouseConfig', function(_, cb)
    cb(Config.Houses)
end)

-- Events

RegisterNetEvent('ta2-houserobbery:server:SetBusyState', function(cabin, house, bool)
    Config.Houses[house]['furniture'][cabin]['isBusy'] = bool
    TriggerClientEvent('ta2-houserobbery:client:SetBusyState', -1, cabin, house, bool)
end)

RegisterNetEvent('ta2-houserobbery:server:enterHouse', function(house)
    local src = source
    if not Config.Houses[house]['opened'] then
        ResetHouseStateTimer(house)
        TriggerClientEvent('ta2-houserobbery:client:setHouseState', -1, house, true)
    end
    TriggerClientEvent('ta2-houserobbery:client:enterHouse', src, house)
    Config.Houses[house]['opened'] = true
end)

RegisterNetEvent('ta2-houserobbery:server:searchFurniture', function(cabin, house)
    local src = source
    local player = TA2Core.Functions.GetPlayer(src)
    local tier = Config.Houses[house].tier
    local availableItems = Config.Rewards[tier][Config.Houses[house].furniture[cabin].type]
    local itemCount = math.random(0, 3)
    if itemCount > 0 then
        for _ = 1, itemCount do
            local selectedItem = availableItems[math.random(1, #availableItems)]
            local itemInfo = TA2Core.Shared.Items[selectedItem.item]

            if not itemInfo.unique then
                local amount = math.random(selectedItem.min, selectedItem.max)
                exports['ta2-inventory']:AddItem(src, selectedItem.item, amount, false, false, 'ta2-houserobbery:server:searchFurniture')
            else
                exports['ta2-inventory']:AddItem(src, selectedItem.item, 1, false, false, 'ta2-houserobbery:server:searchFurniture')
            end
            TriggerClientEvent('ta2-inventory:client:ItemBox', src, itemInfo, 'add')
            Wait(500)
        end
    else
        TriggerClientEvent('TA2Core:Notify', src, Lang:t('error.emty_box'), 'error')
    end
    Config.Houses[house]['furniture'][cabin]['searched'] = true
    TriggerClientEvent('ta2-houserobbery:client:setCabinState', -1, house, cabin, true)
end)

RegisterNetEvent('ta2-houserobbery:server:removeAdvancedLockpick', function()
    local Player = TA2Core.Functions.GetPlayer(source)
    if not Player then return end
    exports['ta2-inventory']:RemoveItem(source, 'advancedlockpick', 1, false, 'ta2-houserobbery:server:removeAdvancedLockpick')
end)

RegisterNetEvent('ta2-houserobbery:server:removeLockpick', function()
    local Player = TA2Core.Functions.GetPlayer(source)
    if not Player then return end
    exports['ta2-inventory']:RemoveItem(source, 'lockpick', 1, false, 'ta2-houserobbery:server:removeLockpick')
end)
